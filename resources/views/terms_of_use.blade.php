<br>제 1 조 (목적)
<br>본 약관은 스트롱애드(이하 “회사”)가 운영하는 리뷰의힘 온라인 웹사이트를 통하여 인플루언서 마케팅 관련 제반 “서비스”를 제공함에 있어 이용 고객(이하 “회원”)과 “회사”간의 권리, 의무 및 책임사항, 기타 필요한 사항을 규정함을 목적으로 합니다.
<br>
<br>제 2 조 (정의)
<br>"약관"에서 사용하는 용어의 정의는 아래와 같습니다.
<br>① “리뷰의힘 서비스”(이하 "서비스")라 함은 "회사"가 제공하는 “광고 중개 캠페인”에 관한 모든 제반 서비스를 의미합니다.
<br>② “광고 중개 캠페인”(이하 “캠페인”)라 함은 “회사”가 정한 방식에 따라 “광고주”가 마케팅 등을 목적으로 “인플루언서”들이 저작물을 등록하거나 그 외의 방법으로 참여 할 수 있게 제공하는 서비스를 말합니다.
<br>③ "인플루언서 회원"(이하 "개인회원")은 “회사”가 운영하는 “사이트”를 통하여 본 약관에 동의하고, “회사” 와 이용 계약을 체결하여 “아이디(ID)”를 부여 받은 개인을 말합니다.
<br>④ “광고주 회원"(이하 "기업 회원”)은 광고를 진행할 개인회원 모집 광고를 목적으로 “회사”가 운영하는 “사이트”를 통하여 본 약관에 동의하고, “회사”와 이용 계약을 체결하여 “아이디(ID)”를 부여 받은 기업과 단체를 말합니다.
<br>⑤ “아이디(ID)”라 함은 “회원” 식별과 “회원”의 “서비스” 이용을 위하여 “회원”이 직접 지정하고 이를 “회사”가 부여하는 문자 또는 숫자의 조합을 말합니다.
<br>⑥ “비밀번호”라 함은 “서비스”를 이용하려는 사람이 “아이디(ID)”를 부여 받은 자와 동일인임을 확인하고, “회원”의 권익을 보호하기 위하여 “회원”이 직접 지정한 문자 또는 숫자의 조합을 말합니다.
<br>
<br>제 3 조 (약관의 게시와 개정)
<br>① 본 약관은 “회사”가 운영하는 “사이트”를 통하여 이를 공지하거나 전자우편 및 기타의 방법으로 “회원”에게 통지함으로써 효력이 발생됩니다.
<br>② “회사”는 관련 법규를 준수하는 범위 내에서 본 약관을 개정할 수 있으며, 사정상 중요한 사유가 발생하여 사전 통지가 불가피 할 경우 약관의 내용을 사전 통지 없이 변경한 후 “회원”에게 변경된 사실에 대하여 통지하여야 합니다.
<br>③ “회사”가 약관을 개정할 경우에는 개정 사유와 적용일자를 명시하고 현행 약관과 함께 본 조 제 ①항의 방식에 따라 개정 약관의 적용 일 최소 7일 이전부터 적용 일 전일까지 공지 또는 통지함으로써 효력이 발생됩니다. 다만, “회원”에게 불리한 조건으로 개정될 경우 최소한 15일 이상의 사전 유예기간을 두고 공지 또는 통지하여야 하며, “회사”는 개정 전 내용과 개정 후 내용을 명확하게 비교하여 이용자가 이해하기 쉽도록 표시하여 “회원”에게 공지 또는 통지하여야 합니다.
<br>④ “회원”이 약관 공지의 날로부터 지정된 유예기간 내에 개정될 약관에 대해 별도의 의사표시를 하지 않을 경우 “회사”는 “회원”이 개정된 약관의 내용에 대해 동의한 것으로 간주합니다.
<br>⑤ “회원”은 “회사”가 개정한 약관의 내용에 동의하지 않을 경우 회원 탈퇴(이하 “가입 해지”)를 요청할 수 있습니다.
<br>⑥ “회사”는 “서비스” 운영에 따라 “사이트” 별 특별 조항을 생성하거나 기존 약관을 개정할 수 있으며, 이 경우 해당 “사이트”를 통하여 본 조 제 ①항의 방식에 따라 “회원”에게 공지 또는 통보 후 약관을 적용할 수 있습니다.
<br>⑦ “회사”는 본 약관의 내용과 상호, 영업장 소재지, 대표자의 성명, 사업자등록번호, 연락처 등을 이용자가 알 수 있도록 게시하거나 기타의 방법으로 이용자에게 공지하여 “회원”이 원할 경우 언제든 본 약관을 확인할 수 있도록 장치를 마련하여야 합니다.
<br>
<br>제 4 조 (약관 외 준칙)
<br>본 약관에서 규정하지 않은 사항에 관해서는 전기통신기본법, 전기통신사업법, 정보통신망 이용촉진 및 정보보호 등에 관한 법률, 전자거래기본법, 신용정보이용 및 보호에 관한 법률, 기타 관련 법령의 규정에 따릅니다.
<br>
<br>제 5 조 (이용계약의 성립)
<br>① “서비스” 이용계약은 “회사”가 제공하는 “서비스”를 받고자 하는 자(이하 “가입신청자”)가 본 약관의 내용을 동의하고, 이용 신청에 대하여 “회사”가 승낙함으로써 성립됩니다.
<br>② 본 조 제 ①항의 승낙은 “회사”가 “가입신청자”의 본 약관에 대한 동의를 확인하고, 전자메일 또는 서면으로 통지함으로써 이용계약이 성립합니다.
<br>③ “가입신청자가” 이용계약 시 “사이트”상의 본 약관을 확인한 후 “동의함” 버튼을 누르면 “회사”의 본 약관 및 개인정보처리방침에 대하여도 동의한 것으로 간주합니다.
<br>
<br>제 6 조 (서비스 이용신청)
<br>① “회원”으로 가입하여 “서비스”를 이용하고자 하는 “가입신청자”는 “회사”에서 요구하는 제반 정보를 가입신청서 양식에 맞춰 하자 없이 작성하여야 합니다.
<br>② 본 조 제 ①항의 가입신청서에 실제 본인의 정보와는 달리 허위 정보로 작성한 경우 “서비스” 이용에 대한 제한을 받을 수 있을 뿐만 아니라, “가입신청자”는 일체의 권리를 주장할 수 없습니다.
<br>③ 타인의 명의 및 개인 정보를 도용하여 가입신청을 한 “회원”의 “아이디(ID)”는 삭제 처리되며, 관계법령에 따라 처벌을 받을 수 있습니다.
<br>
<br>제 7 조 (이용신청의 승낙과 제한)
<br>① “회사”는 “가입신청자”의 가입 승낙 처리 시 특별한 경우를 제외하고는 신청 순서에 따라 “서비스” 이용에 승낙하는 것을 원칙으로 합니다.
<br>② "개인 회원" 가입 시 신청 양식에서 요구한 정보를 작성하고 '서비스 이용약관'과 '개인정보 수집 및 이용'에 동의하는 것은 이 약관 및 '개인정보처리방침'의 내용을 숙지하고, 회사의 각종 서비스 정책과 공지사항을 준수하는 것에 대해 동의하는 것으로 봅니다.
<br>③ “회사”는 아래 각 호의 경우에 승낙을 철회하거나 거절할 수 있습니다.
<br>a. 가입신청자가 이 약관에 의하여 이전에 "개인 회원" 자격을 상실한 사실이 있는 경우. 단, "개인 회원" 자격 상실 후 회사의 "개인 회원" 재가입 승낙을 얻은 경우에는 예외로 합니다.
<br>b. 등록을 신청한 사람이 만 14세 미만일 경우
<br>c. 이용자의 귀책사유로 인하여 승인이 불가능하거나 기타 규정한 제반 사항을 위반하며 신청하는 경우
<br>d. "개인 회원" 가입 시 기재사항 및 기타 기재사항의 등록 내용에 허위, 기재누락, 도용, 오류 표기가 있는 경우
<br>④ 회사는 서비스를 이용하는 "개인 회원"의 “활동점수”를 검토하여, 서비스 이용 범위에 차등을 둘 수 있습니다.
<br>⑤ "개인 회원"는 불법, 편법, 서비스 운영 정책의 우회를 목적으로 복수의 개인 회원" 가입을 해서는 안 됩니다.
<br>
<br>제 8 조 (서비스의 내용)
<br>① “회사”는 제 2조 ①항의 “사이트”를 통하여 제 2조 ②항의 “서비스”를 제공할 수 있으며, 그 내용은 다음 각 호와 같습니다.
<br>1. 리뷰 전략 등록과 리뷰 전략 열람 서비스
<br>2. 체험단 신청 관리 
<br>3. 온라인 마케팅 중개와 관련된 제반 서비스
<br>4. 이용자간의 교류와 소통에 관련한 서비스
<br>5. “회사”가 제휴 계약 등을 통해 “회원”에게 제공하는 일체의 서비스
<br>6. 리워드 광고 서비스
<br>7. 포인트/캐시 서비스
<br>② “회사”는 “사이트” 운영에 필요한 경우 “서비스”의 내용을 추가 또는 변경할 수 있으며, 이 경우 “회사”는 추가 또는 변경되는 내용을 “회원”에게 제 3조의 ①항에 따라 공지 또는 통보합니다.
<br>
<br>제 9 조 (개인 회원 정보, 리뷰 전략 노출)
<br>① “개인 회원”의 리뷰전략은 “개인 회원”이 리뷰전략을 작성후 등록하면 “기업 회원”의 이력서 열람에 동의한 것으로 간주하며 “회사”는 “회원”에게 무/유료로 이력서 열람이 가능하도록 제공할 수 있습니다. 
<br>② “회사”는 “개인 회원”이 회원 정보, 리뷰전략 상의 내용을 자유롭게 작성할 수 있도록 합니다.
<br>③ “회사”는 안정적인 “서비스”를 제공하기 위해 테스트 및 모니터링 용도에 한하여 “개인회원”의 이력서 정보를 열람할 수 있습니다.
<br>
<br>제 10 조 (캠페인 리뷰의 저작권)
<br>① "개인회원"의 모든 “리뷰” 저작권은 "개인회원"에게 있으며, 등록한 “리뷰”의 저작권 침해를 비롯한 법적 책임은 "개인회원" 본인에게 있습니다.
<br>② "개인회원"은 “리뷰”에 대하여 사용료 없는 영구적인 사용권을 “회사” 및 해당 “캠페인”의 “기업회원”에 부여합니다. “회사” 및 “기업회원”은 "개인회원"가 탈퇴한 후에도 사용권을 가지며, 여기에는 리뷰의 복제, 전시, 배포, 편집, 2차적 저작물의 작성 권한 등이 포함됩니다. 또한 리뷰는 검색결과 내지 서비스 관련 프로모션 등에 노출될 수 있으며, “회사” 및 “기업회원”은 “리뷰”을 마케팅에 활용할 수 있습니다.
<br>③ “회사” 및 “기업회원”은 "개인회원"가 등록한 “리뷰”의 링크 또는 “리뷰”, “리뷰” 내 멀티미디어를 각각 또는 결합하여 마케팅 등에 활용할 수 있습니다.
<br>④ “회사” 및 “기업회원”은 제공된 리뷰의 취지를 벗어나지 않는 정도의 편집, 수정, 가공 등을 할 수 있으며 출처의 표기를 제외할 수도 있습니다.
<br>
<br>제 11 조 (캠페인 리뷰의 관리)
<br>① "개인회원"은 “리뷰”을 최소 6개월 동안 게시하여 해당 “프로젝트”의 “기업회원” 및 제3자가 콘텐츠를 열람할 수 있도록 하여야 합니다.
<br>② “회사” 및 “기업회원”은 아래 각 호에 해당하는 경우, "개인회원"에게 제공한 “포인트“ 및 기타 사례에 대한 환수 조치 및 법적 조치를 취할 수 있습니다.
<br>a. “리뷰” 유지기간 내에 "개인회원"가 임의로 “리뷰”을 삭제 및 숨김 등 접근 불가능하게 변경
