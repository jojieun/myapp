<br>제 21 조 (회사의 의무)
<br>① “회사”는 본 약관에서 정한 바에 따라 지속적, 안정적으로 “서비스”를 제공 할 수 있도록 최선의 노력을 다해야 합니다.
<br>② “회사”는 특별한 사정이 없는 한 “회원”이 “서비스” 이용 신청 후, 48시간 이내에 “서비스”를 이용할 수 있도록 합니다.
<br>③ “회사”는 “서비스”에 관련된 설비를 항상 운용할 수 있는 상태로 유지, 보수하고, 장애가 발생하는 경우 지체 없이 이를 수리 및 복구할 수 있도록 최선의 노력을 다하여야 합니다. 다만, 천재지변 등 예측하지 못한 일이 발생하거나 시스템의 장애가 발생하여 “서비스”가 중단될 경우 이에 대한 손해는 “회사”가 책임을 지지 않으며, 이 경우 자료의 복구나 정상적인 “서비스” 지원이 되도록 최선을 다할 의무가 있습니다.
<br>④ “회사”는 “서비스”와 관련한 “회원”의 의견이나 불만사항이 접수되는 경우, 그 내용이 타당하다고 판단 될 경우 상황에 맞는 적절한 조치를 취하여야 합니다.
<br>⑤ “회원”이 등록한 자료로 인하여 “사이트”의 원활한 운영에 영향을 미친다고 판단될 시, 등록된 모든 자료를 “회원”의 사전 동의 없이 삭제할 수 있습니다.
<br>⑥ “회사”는 유료 결제와 관한 결제 정보를 5년 이상 보존합니다. 다만, “회원”자격이 없는 “회원”은 예외로 합니다.
<br>
<br>제 22 조 (회원의 의무)
<br>① “회원”은 관계법령과 본 약관의 규정 및 기타 “회사”가 공지, 통지하는 사항을 준수하여야 하며, 기타 “회사”의 업무에 방해되는 행위, “회사”의 명예를 손상시키는 행위, 타인에게 피해를 주는 행위를 해서는 안됩니다.
<br>② “회원”은 “서비스”를 이용함으로써 얻은 정보를 채용 이외의 목적으로 “회사”의 사전 승낙 없이 복제, 송신, 출판, 전송, 배포, 방송, 기타 방법에 의하여 영리목적으로 이용하거나 제 3자에게 이용하게 하여서는 안됩니다.
<br>③ “회원”이 신청한 유료서비스는 신청 또는 등록과 동시에 “회사”와 채권, 채무 관계가 발생하며, “회원”은 이에 대한 요금을 지정한 기일 내에 납부하여야 합니다.
<br>④ “회원”이 결제 수단으로 신용카드를 사용할 경우 “비밀번호” 등 정보 유실 방지는 “회원” 스스로 관리해야 하며 “사이트”의 결함에 따른 정보유실의 발생에 대한 책임은 “회원”의 의무에 해당하지 않습니다.
<br>⑤ “회원”에게 부여된 “아이디(ID)”와 “비밀번호”의 관리 소홀, 부정사용에 의하여 발생하는 모든 결과에 대한 책임은 “회원”에게 있으며 “회원”은 자신의 “아이디(ID)”나 “비밀번호”가 부정하게 사용되었다는 사실을 발견한 경우 즉시 “회사”에 알려야 하며, 이를 알리지 않아 발생하는 모든 결과에 대한 책임은 “회원”에게 있습니다.
<br>⑥ “회원”은 “회사”가 공지사항에 게시하거나 별도로 공지한 이용제한 사항을 준수하여야 합니다.
<br>⑦ “회원”은 “회사”의 사전승낙 없이는 “서비스”를 이용하여 영업활동을 할 수 없으며, 그 영업활동의 결과와 “회원”이 약관에 위반한 영업활동을 하여 발생한 결과에 대하여 “회사”는 책임을 지지 않습니다. “회원”은 이와 같은 영업활동으로 “회사”에 손해를 입힌 경우 “회원”은 “회사”에 대하여 손해배상의무를 부담하여야 합니다.
<br>⑧ “회원”은 “회사”의 명시적인 동의가 없는 한 “서비스”의 이용권한, 기타 이용계약상 지위를 타인에게 양도, 증여할 수 없으며, 이를 담보로 제공할 수 없습니다.
<br>⑨ “회원”은 본 “서비스”를 건전한 구인 구직 이외의 목적으로 사용해서는 안되며 이용 중 다음 각 호의 행위를 해서는 안 됩니다.
<br>a. 범죄 행위를 목적으로 하거나 기타 범죄행위와 관련된 행위
<br>b. 공공질서 및 미풍양속에 위반되는 내용 등을 전송, 게시, 전자메일 또는 기타의 방법으로 타인에게 유포하는 행위
<br>c. 다른 “회원”의 “아이디(ID”)와 “비밀번호”, 기타 개인정보 등을 도용하고 부정 사용하는 행위
<br>d. 타인의 명예를 훼손하거나 모욕하는 행위
<br>e. 타인의 지적재산권 등의 권리를 침해하는 행위
<br>f. 해킹행위 또는 바이러스의 유포 행위
<br>g. 타인의 의사에 반하여 광고성 정보 등 일정한 내용을 계속적으로 전송하는 행위
<br>h. “서비스”의 안정적인 운영에 지장을 주거나 줄 우려가 있다고 판단되는 행위
<br>i. 그 밖에 선량한 풍속, 기타 사회질서를 해하거나 관계법령에 위반하는 행위
<br>⑩ “회원”은 이용신청서의 기재내용 중 변경된 내용이 있을 경우 해당 절차를 거쳐 그 내용을 “회사”에 통지하여야 합니다. 만약, 변경된 내용을 통지하지 않아 발생하는 피해에 대하여 “회사”는 책임지지 않습니다.
<br>
<br>제 23 조 (개인정보 유효기간제)
<br>① “회사”는 운영하는 “사이트” 에 가입한 “회원”이 1년 간 로그인 등 이용 기록이 없을 경우 “회원”의 계정을 “휴면계정”처리하여 회원가입 단계에서 수집된 일체의 개인정보를 안전하게 별도보관 하며, 이 경우 "회사"는 휴면계정 처리 예정일로부터 30일 이전에 해당 사실을 전자메일, 서면, SMS 중 하나의 방법으로 "회원"에게 사전 통지하고 "회원"이 로그인 등으로 "사이트" 이용의사표시를 한 경우에는 이용의사표시 일로부터 1년간 "회원" 의 계정을 “휴면계정”처리 하지 않습니다. 또한 “회사”는 “휴면계정”처리된 회원의 개인정보를 이용,제공 하지 않습니다.
<br>
<br>제 24 조 (회원의 계약 해지 / 서비스 중지 / 자료 삭제)
<br>① "개인회원"은 참여 중인 “캠페인”이 존재하지 않을 경우, 언제든 서비스 내의 회원탈퇴 기능을 이용하여 이용계약 해지 신청을 할 수 있습니다.
<br>② "개인회원"이 계약을 해지하는 경우 관련법 및 개인정보처리방침에 따라 “회사”가 "개인회원"의 정보를 보유하는 경우를 제외하고는 해지 즉시 "개인회원"의 개인정보가 소멸됩니다.
<br>③ "개인회원"가 이 약관의 의무를 위반하거나 서비스의 정상적인 운영을 방해한 경우 “회사”는 일방적으로 계약을 해지할 수가 있습니다.
<br>④ "개인회원"가 1년 동안 “서비스”에 로그인한 기록이 없는 경우 휴면 계정으로 전환되며 계약이 해지 됩니다.
<br>⑤ 본 계약이 해지 된 경우 "개인회원"가 보유한 “포인트”를 환급 받을 권리 및 적립된 “포인트“ 등은 소멸되며 ”회사“는 계약이 해지 된 이후의 ”포인트“ 지급에 대한 요구, 문의 등의 처리를 일절 행하지 않는 것으로 합니다.
<br>⑥ 본 계약이 해지 된 경우에도 "개인회원"가 캠페인에 등록한 리뷰는 삭제되지 않습니다.
<br>⑦ 본 계약이 해지 된 경우에도 "개인회원"가 공용 게시판에 등록한 게시물, 댓글 등은 삭제되지 않습니다.
<br>
<br>제 25 조 기업회원의 법적 권한
<br>“개인회원”은 아래와 같은 사항에 한 해 “기업회원”에게 별도 연락을 취하지 않는 경우, “기업회원”의 손해배상 등의 법적 조치를 받을 수 있습니다.
<br>1. “개인회원”이 “캠페인”의 리뷰을 무단 미제출 하거나 혹은 가이드라인에 따라 “리뷰”을 제대로 발행하지 않은 경우
<br>2. “개인회원”가 “캠페인”의 진행을 위한 제품을 수령한 후, 타당한 이유 없이 제품을 반송하거나 “캠페인” 참여를 무단 취소하는 경우
<br>3. “개인회원”의 개인 착오로 인해 “리뷰” 제출 일정이 지연되는 경우, 혹은 “캠페인” 취소 요청을 하는 경우
<br>
<br>제 26 조 (손해배상 및 면책)
<br>① “회사”는 천재지변 또는 이에 준하는 불가항력으로 인하여 “서비스”를 제공할 수 없는 경우 “서비스” 제공에 관한 책임이 면제됩니다.
<br>② “회사”는 “서비스” 이용과 관련하여 “회원”에게 발생한 손해 가운데 “회원”의 고의, 과실 및 “회원”의 귀책사유로 인한 “서비스” 이용의 장애 및 손해에 대하여 책임을 지지 않습니다.
<br>③ “회사”는 “회원”이 “서비스”에 게재한 정보, 자료, 사실의 신뢰도, 정확성 등 내용에 관하여는 책임을 지지 않습니다.
<br>④ “회원”이 본 약관의 제 7조, 제 19조, 제 21조 등의 규정에 위반한 행위로 “회사” 및 제3자에게 손해를 입히거나 “회원”의 책임이 있는 사유에 의해 “회사” 및 제3자에게 손해를 입힌 경우에는 “회원”은 그 손해를 배상하여야 할 책임이 있습니다.
<br>⑤ “회사”는 본 약관의 제 18조 등의 규정에 위반한 행위로 이용자에게 손해가 발생한 경우 “회사”는 그 손해를 배상할 의무가 있습니다.
<br>⑥ “회사”는 “회원”이 “서비스”를 이용하여 기대하는 수익을 얻지 못하거나 상실한 것에 대하여 책임을 지지 않으며, “회사”가 무료로 제공하는 “서비스”의 이용과 관련하여 개인정보처리방침에서 정하는 내용에 위반하지 않은 한 어떠한 손해도 책임지지 않습니다.
<br>
<br>제 27 조 (회원의 개인정보보호)
<br>“회사”는 “회원”의 개인정보보호를 위하여 노력해야하며 “회원”의 개인정보보호에 관해서는 정보통신망이용촉진 및 정보보호 등에 관한 법률에 따르고, “사이트”에 "개인정보처리방침"을 고지합니다.
<br>
<br>제 28 조 (신용정보의 제공 활용 동의)
<br>① “회사”가 “회원”가입과 관련하여 취득한 “회원”의 신용정보를 타인에게 제공하거나 활용하고자 할 때에는 “신용정보 이용 및 보호에 관한 법률” 제23조의 규정에 따라 사전에 그 사유 및 해당기관 또는 업체명 등을 밝히고 해당 “회원”의 동의를 얻어야 합니다.
<br>② 본 “서비스”와 관련하여 “회사”가 “회원”으로부터 “신용정보 이용 및 보호에 관한 법률”에 따라 타인에게 정보제공 활용에 동의를 얻은 경우 “회원”은 “회사”가 신용정보 사업자 또는 신용정보 집중기관에 정보를 제공하여 “회원”의 신용을 판단하기 위한 자료로 활용하거나, 공공기관에서 정책자료로 활용되도록 정보를 제공하는 데 동의한 것으로 간주합니다.
<br>③ “회사”는 원활한 “서비스” 이용과 허위정보로 인한 피해를 방지하기 위해 NICE신용평가정보(주) 를 통해 실명인증을 시행합니다.
<br>
<br>제 29 조 (분쟁의 해결)
<br>① “회사”와 “회원”은 “서비스”와 관련하여 발생한 분쟁을 원만하게 해결하기 위하여 필요한 노력을 하여야 합니다.
<br>② 전항의 노력에도 불구하고, 분쟁에 대해 소송이 제기될 경우 민사소송법 등 법령에 정한 절차에 따른 법원을 관할 법원으로 합니다.
